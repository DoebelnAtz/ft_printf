char	*ft_strchr(const char *s, int c)
{
	char	*ch;
	int		i;

	i = 0;
	ch = 0;
	while (s[i] != '\0')
	{
		if (s[i] == c)
		{
			ch = (char *)&s[i];
			return (ch);
		}
		i++;
	}
	if (c == 0)
	{
		ch = (char *)&s[i];
	}
	return (ch);
}
