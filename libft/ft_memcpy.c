#include "libft.h"

void	*ft_memcpy(void *dst, const void *src, size_t n)
{
	unsigned char	*s;
	unsigned char	*d;

	s = (unsigned char *)src;
	d = (unsigned char *)dst;
	if (!n || src == dst)
		return (dst);
	while (n--)
	{
		*d++ = *s++;
	}
	return (dst);
}
